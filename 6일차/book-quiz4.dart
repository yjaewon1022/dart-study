// 컴퓨터와 가위바위보를 하는 프로그램을 구현하고자 합니다.
// 다음의 코드를 참고하여, 해당 내용과 똑같이 동작하는 프로그램을 구현해야 합니다.
// 낼 것을 선택해 주세요 (가위, 바위, 보 중 하나 입력): 가위
// 컴퓨터는 "보" (을)를 냈습니다.
// 사용자는 "가위" (을)를 냈습니다.
// 사용자가 이겼습니다.
// 점수를 확인하시겠습니까? 네
// 컴퓨터 : 0 vs 사용자 : 1
// 낼 것을 선택해 주세요 (가위, 바위, 보 중 하나 입력): 가위
// 컴퓨터는 "가위" (을)를 냈습니다.
// 사용자는 "가위" (을)를 냈습니다.
// 이긴 사람이 없습니다.
// 낼 것을 선택해 주세요 (가위, 바위, 보 중 하나 입력): 보
// 컴퓨터는 "가위" (을)를 냈습니다.
// 컴퓨터는 "가위" (을)를 냈습니다.
// 사용자는 "보" (을)를 냈습니다.
// 사용자가 졌습니다.
// 점수를 확인하시겠습니까? 아니오
// 낼 것을 선택해 주세요 (가위, 바위, 보 중 하나 입력): 보
// ...
// (이긴 경우)
// 사용자가 3 : 1 로 가위바위보를 승리했습니다.
// 프로그램을 종료합니다.
// (진 경우)
// 사용자가 2 : 3 로 가위바위보를 패배했습니다.
// 프로그램을 종료합니다.

import 'dart:math';

void main() {
  final random = Random();
  final options = ['가위', '바위', '보'];

  // 0, 1, 2 중 하나를 랜덤으로 선택
  String computerChoice = options[random.nextInt(3)];

  print('컴퓨터의 선택: $computerChoice');
}
